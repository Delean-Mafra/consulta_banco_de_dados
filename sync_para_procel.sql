CREATE PROCEDURE "ECF_REDUCAO_DETALHE_AUX"
(IN "COD_ECF_REDUCAO" INTEGER)
declare variable TIPO_TOTALIZADOR varchar(100);
declare variable TOTALIZADOR_PARCIAL varchar(100);
declare variable TOT_AUX varchar(100);
declare variable COD_ECF_REDUCAO_DETALHE integer;
BEGIN
  FOR SELECT COD_ECF_REDUCAO_DETALHE,
    CASE
    WHEN ((TIPO_TOTALIZADOR = 'F') OR (TIPO_TOTALIZADOR = 'F1') OR (TIPO_TOTALIZADOR = 'F2') OR (TIPO_TOTALIZADOR = 'F3')) THEN 'F'
    WHEN ((TIPO_TOTALIZADOR = 'I') OR (TIPO_TOTALIZADOR = 'I1') OR (TIPO_TOTALIZADOR = 'I2') OR (TIPO_TOTALIZADOR = 'I3')) THEN 'I'
    WHEN ((TIPO_TOTALIZADOR = 'N') OR (TIPO_TOTALIZADOR = 'N1') OR (TIPO_TOTALIZADOR = 'N2') OR (TIPO_TOTALIZADOR = 'N3')) THEN 'N'
    WHEN ((TIPO_TOTALIZADOR = 'T')) THEN 'T'|| (SUBSTRING(TOTALIZADOR_PARCIAL FROM 4 FOR 4))
    ELSE TIPO_TOTALIZADOR
    END
  FROM ECF_REDUCAO_DETALHE
  WHERE COD_ECF_REDUCAO = :COD_ECF_REDUCAO
  INTO :COD_ECF_REDUCAO_DETALHE, :TOT_AUX
  DO
  begin
    UPDATE ECF_REDUCAO_DETALHE SET TOTALIZADOR_AUX = :TOT_AUX WHERE COD_ECF_REDUCAO_DETALHE = :COD_ECF_REDUCAO_DETALHE;
    UPDATE ECF_REDUCAO_DETALHE SET TOTALIZADOR_PARCIAL = :TOT_AUX WHERE COD_ECF_REDUCAO_DETALHE = :COD_ECF_REDUCAO_DETALHE AND COALESCE(TOTALIZADOR_PARCIAL,'')='';
  end
END^
