CREATE PROCEDURE "INTEGRACAO_CARGA_TOTAL_PRODUTOS"
DECLARE variable COD_TAB_PRECO integer;
DECLARE variable COD_FC integer;
DECLARE variable COD_MATERIAL integer;
BEGIN
  SELECT cast(coalesce(VALOR, '0') AS integer) FROM PARAMETRO WHERE NOME_PARAMETRO = 'COD_FRENTE_CAIXA_WEB' INTO :COD_FC;
  SELECT COD_TABELA_PRECO FROM FRENTE_CAIXA WHERE COD_FRENTE_CAIXA = :COD_FC INTO :COD_TAB_PRECO;
  FOR SELECT distinct MTP.COD_MATERIAL
      FROM ALMMATERIAL MAT
      inner join MATERIAL_TAB_PRECO MTP on MTP.COD_MATERIAL = MAT.COD_MATERIAL AND MTP.COD_TABELA_PRECO = :COD_TAB_PRECO
      WHERE ATV_ALMMATERIAL = 'V'
