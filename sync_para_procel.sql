    BEGIN
        -- Calcula o total de anos de fundação
        NEW.CODIGO_ANTIGO = EXTRACT(YEAR FROM CURRENT_DATE) - EXTRACT(YEAR FROM NEW.DATA_NASC_FORNECEDOR);
        
        -- Caso o mês ou dia da fundação ainda não tenha ocorrido neste ano, subtrai 1
        IF (EXTRACT(MONTH FROM CURRENT_DATE) < EXTRACT(MONTH FROM NEW.DATA_NASC_FORNECEDOR)) THEN
        BEGIN
            NEW.CODIGO_ANTIGO = NEW.CODIGO_ANTIGO - 1;
        END
        ELSE IF (EXTRACT(MONTH FROM CURRENT_DATE) = EXTRACT(MONTH FROM NEW.DATA_NASC_FORNECEDOR) AND EXTRACT(DAY FROM CURRENT_DATE) < EXTRACT(DAY FROM NEW.DATA_NASC_FORNECEDOR)) THEN
        BEGIN
            NEW.CODIGO_ANTIGO = NEW.CODIGO_ANTIGO - 1;
        END
    END
END^
