                   cast(sum(cast(C460.VL_ITEM6 as numeric(12,6))) - sum(cast(C460.VL_ITEM as numeric(12,2))) as numeric(12,2)) as DIF
            from SPEDTMP_C460 C460
            WHERE COD_ECF_REDUCAO = :COD_ECF_REDUCAO
            --WHERE C460.num_doc='33456'
            group by 1,2) TB
      where DIF <> 0
      into :ID, :totalizador_parcial, :DIF
  do
  begin
    for
        select first 1 C460_.PK, C460_.VL_ITEM
        from SPEDTMP_C460 C460_
        where C460_.ID = :ID and C460_.totalizador_parcial = :totalizador_parcial
        order by C460_.VL_ITEM desc, C460_.ALIQ_ICMS desc
        into :PK , :VL_I
    do
    begin
      VL_I = VL_I + :DIF;
      update SPEDTMP_C460
      set VL_ITEM = :VL_I/*, VL_ITEM6 = :VL_I*/
      where PK = :PK;
    end
  end

end^
