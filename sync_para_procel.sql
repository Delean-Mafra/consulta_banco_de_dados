-- PROCEDURE: SP_MIGRAR_BENEFICIOS
SET TERM ^ ;
CREATE PROCEDURE "SP_MIGRAR_BENEFICIOS"
(OUT "REGISTROS_MIGRADOS" INTEGER)
DECLARE VARIABLE V_COD_NATUREZA INTEGER;
DECLARE VARIABLE V_COD_SUBST_TRIBUTARIA INTEGER;
DECLARE VARIABLE V_CBENEF VARCHAR(10);
DECLARE VARIABLE V_COD_BENEFICIO INTEGER;
BEGIN
    REGISTROS_MIGRADOS = 0;
    
    /* Migrar dados da tabela NATUREZA_BENEFICIO_FISCAL para NATUREZA_OPER_BENEF_FISCAL */
    FOR SELECT 
            NBF.COD_NATUREZA_OPER,
            NBF.COD_SUBST_TRIBUTARIA,
            NBF.CBENEF
        FROM 
            NATUREZA_BENEFICIO_FISCAL NBF
        INTO 
            :V_COD_NATUREZA, :V_COD_SUBST_TRIBUTARIA, :V_CBENEF
    DO
    BEGIN
        /* Verificar se já existe um benefício com esse CBENEF */
        SELECT FIRST 1 
            BF.COD_BENEFICIO 
        FROM 
            BENEFICIO_FISCAL BF 
        WHERE 
            BF.ID_BENEFICIO = :V_CBENEF
        INTO 
            :V_COD_BENEFICIO;
        
        /* Se não existe, criar */
        IF (V_COD_BENEFICIO IS NULL) THEN
        BEGIN
            /* Criar novo benefício fiscal */
            INSERT INTO BENEFICIO_FISCAL (
                ID_BENEFICIO, 
                NOME_BENEFICIO, 
                ICMS_SUBST_TRIB
            )
            VALUES (
                :V_CBENEF,
                'Benefício ' || :V_CBENEF,
                'S'
            );
            
            /* Obter o ID gerado */
            V_COD_BENEFICIO = GEN_ID(SEQ_BENEFICIO_FISCAL, 0);
        END
        
        /* Associar à natureza */
        IF (NOT EXISTS(
            SELECT 1 FROM NATUREZA_OPER_BENEF_FISCAL 
            WHERE COD_NATUREZA_OPER = :V_COD_NATUREZA 
            AND COD_BENEFICIO = :V_COD_BENEFICIO
        )) THEN
        BEGIN
            INSERT INTO NATUREZA_OPER_BENEF_FISCAL (
                COD_NATUREZA_OPER, 
                COD_BENEFICIO
            ) VALUES (
                :V_COD_NATUREZA, 
                :V_COD_BENEFICIO
            );
            
            REGISTROS_MIGRADOS = REGISTROS_MIGRADOS + 1;
        END
    END
    
    SUSPEND;
END^
SET TERM ; ^

