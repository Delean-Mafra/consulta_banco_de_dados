-- PROCEDURE: AJUSTA_C460
SET TERM ^ ;
CREATE PROCEDURE "AJUSTA_C460"
(IN "COD_ECF_REDUCAO" INTEGER)
declare variable TOTALIZADOR_AUX varchar(100);
declare variable DIF double precision;
declare variable SOMA double precision;
declare variable PK integer;
declare variable ID integer;
declare variable VL_ITEM double precision;
declare variable DIFTMP double precision;
begin
  for select TOTALIZADOR_AUX, VALOR_ERD - SUM_VL_ITEM as DIF
      from (

      select TOTALIZADOR_AUX, SUM_VL_ITEM,
             (select first 1 VALOR_ACUMULADO
              from ECF_REDUCAO_DETALHE ERD
              where ERD.COD_ECF_REDUCAO = TX.COD_ECF_REDUCAO and
                    ERD.TOTALIZADOR_AUX = TX.TOTALIZADOR_AUX) VALOR_ERD
      from (select COD_ECF_REDUCAO, TOTALIZADOR_AUX, sum(VL_ITEM) SUM_VL_ITEM
            from SPEDTMP_C460 C460
            where COD_ECF_REDUCAO = :COD_ECF_REDUCAO
            group by 1, 2) TX

      ) TX2
      where --tx2.sum_vl_item <> valor_erd and
            VALOR_ERD > 0 and
            ((SUM_VL_ITEM - VALOR_ERD > 0.001) or (SUM_VL_ITEM - VALOR_ERD < -0.001))
      into :TOTALIZADOR_AUX, :DIF
  do
  begin

    for select PK, ID, C460.VL_ITEM
        from SPEDTMP_C460 C460
        where C460.COD_ECF_REDUCAO = :COD_ECF_REDUCAO and
              TOTALIZADOR_AUX = :TOTALIZADOR_AUX
        order by C460.VL_ITEM desc
        into :PK, :ID, :VL_ITEM
    do
    begin
      if ((DIF > 0.001) or (DIF < -0.001)) then
      begin
        if ((DIF < 0) and
            (DIF * -1 > VL_ITEM - 0.001)) then
        begin
          DIFTMP = (VL_ITEM - 0.01) * -1;
          DIF = DIF - DIFTMP;
        end
        else
        begin
          DIFTMP = DIF;
          DIF = 0;
        end
        update SPEDTMP_C460
        set VL_ITEM = round((VL_ITEM + :DIFTMP) * 100) / 100
        where PK = :PK;
        /* insert into LOG_AJUSTE_C460 (PK, ID, DIF)  values (:PK, :ID, :DIFTMP);*/
        update SPEDTMP_C460
        set VL_DOC = (select sum(VL_ITEM)
                      from SPEDTMP_C460
                      where ID = :ID)
        where ID = :ID;
      end
    end
  end

  for select ID, SOMA
      from (select C460.ID, C460.COD_ECF_REDUCAO, C460.NUM_DOC, C460.VL_DOC, sum(C460.VL_ITEM) SOMA
            from SPEDTMP_C460 C460
            group by 1, 2, 3, 4) TB
      where VL_DOC <> SOMA
      into :ID, :SOMA
  do
  begin
    update SPEDTMP_C460
    set VL_DOC = :SOMA
    where ID = :ID;
  end

end^
SET TERM ; ^

